#!/usr/bin/env python3
# -*- coding: utf-8 -*-

"""
Run PolicyRepair on the mixed-service performance experiment datasets
and collect Iters, T_solve, and T_total for each case.

Datasets are generated by tools/generate_perf_mixed.py under:
  experiments/perf/mixed/
    statements_*.json
    wildcards_*.json
    universe_*.json

Metrics are written to:
  outputs/perf_mixed_metrics.csv
"""

from __future__ import annotations

import copy
import csv
import time
from dataclasses import dataclass
from pathlib import Path
from typing import List, Tuple
import sys


ROOT = Path(__file__).resolve().parents[1]
if str(ROOT) not in sys.path:
    sys.path.insert(0, str(ROOT))

import policy_repair_z3_multi_io as pr  # noqa: E402


EXP_DIR = ROOT / "experiments" / "perf" / "mixed"
OUT_DIR = ROOT / "outputs"
OUT_CSV = OUT_DIR / "perf_mixed_metrics.csv"


@dataclass
class CaseMetrics:
    case: str
    family: str
    level: int
    iters: int
    t_solve: float
    t_total: float
    sat: bool


def parse_family_and_level(path: Path) -> Tuple[str, int]:
    stem = path.stem
    if "_" in stem:
        prefix, num_str = stem.split("_", 1)
        family = prefix
        try:
            level = int(num_str)
        except ValueError:
            level = -1
    else:
        family = "other"
        level = -1
    return family, level


def cegis_repair_profiled(exp: pr.Experiment, verbose: bool = False) -> Tuple[List[pr.Policy], int, float, float, bool]:
    """
    CEGIS loop with profiling, using the same semantics as the main script.
    """
    W: List[pr.Request] = []
    # Apply the same merge-based preprocessing as the main script.
    try:
        merged_policies = pr.merge_policies_for_repair(exp.policies)
    except ValueError as e:
        if verbose:
            print(f"[Merge] Conflict detected: {e}")
            print("[Merge] Refusing to repair this input policy set.")
        return copy.deepcopy(exp.policies), 0, 0.0, 0.0, False

    P = copy.deepcopy(merged_policies)
    t_solve = 0.0
    start_total = time.perf_counter()
    iters = 0
    sat = False

    for it in range(1, exp.max_iters + 1):
        iters = it
        res = pr.verify(P, exp.p1, exp.p2, exp.baseline)
        if res.sat:
            if verbose:
                print(f"[Iter {it}] SAT: all properties satisfied.")
            sat = True
            break

        w = res.witness
        assert w is not None
        W.append(w)
        if verbose:
            print(f"[Iter {it}] Witness({res.kind}): principal={w.principal}, action={w.action}, resource={w.resource}")

        # Rebuild candidates from all witnesses on current P
        E: List[pr.Edit] = []
        for u in W:
            E.extend(pr.generate_candidates(P, u, exp.baseline, exp.weights))

        if verbose:
            print(
                f"[Iter {it}] Candidates: {len(E)}  "
                + ", ".join(f"{e.kind}:{e.weight}@pol{e.target.policy_idx}:{e.target.sid}" for e in E)
            )

        t0 = time.perf_counter()
        chosen = pr.pick_min_cost_edits(P, W, exp.baseline, E)
        t_solve += time.perf_counter() - t0

        if not chosen:
            if verbose:
                print(f"[Iter {it}] No feasible edit set found. Stop.")
            break

        if verbose:
            for e in chosen:
                print(
                    f"  -> Apply {e.kind} on policy[{e.target.policy_idx}] "
                    f"sid={e.target.sid}: {e.payload}  (w={e.weight})"
                )
        P = pr.apply_edits(P, chosen)

    t_total = time.perf_counter() - start_total
    return P, iters, t_solve, t_total, sat


def main() -> None:
    OUT_DIR.mkdir(parents=True, exist_ok=True)

    if not EXP_DIR.is_dir():
        raise SystemExit(f"Mixed perf experiments not found under {EXP_DIR}. Run tools/generate_perf_mixed.py first.")

    cases: List[CaseMetrics] = []

    json_files = sorted(EXP_DIR.glob("*.json"))
    if not json_files:
        raise SystemExit(f"No JSON experiments found in {EXP_DIR}.")

    for path in json_files:
        rel = path.relative_to(ROOT).as_posix()
        family, level = parse_family_and_level(path)

        exp = pr.PolicyLoader.load_json(str(path))
        _, iters, t_solve, t_total, sat = cegis_repair_profiled(exp, verbose=False)

        cases.append(
            CaseMetrics(
                case=rel,
                family=family,
                level=level,
                iters=iters,
                t_solve=t_solve,
                t_total=t_total,
                sat=sat,
            )
        )

    with OUT_CSV.open("w", newline="", encoding="utf-8") as f:
        writer = csv.writer(f)
        writer.writerow(["case", "family", "level", "iters", "t_solve", "t_total", "sat"])
        for m in cases:
            writer.writerow(
                [m.case, m.family, m.level, m.iters, f"{m.t_solve:.6f}", f"{m.t_total:.6f}", int(m.sat)]
            )

    print(f"Wrote {len(cases)} rows of mixed perf metrics to {OUT_CSV}")


if __name__ == "__main__":
    main()
